                <h1>O financiamento climático sob a ótica da justiça ambiental global: três casos brasileiros</h1>
                <p>Para identificar todas as concessões de financiamento climático que os países
doadores declararam ter dirigido ao Brasil durante o período escolhido
(1998 - 2008), conduzimos uma busca cruzada entre os bancos de dados da
Organização para a Cooperação e o Desenvolvimento Econômico - OCDE e
da plataforma AidData (que é o maior e mais completo banco de dados sobre
auxílio ao desenvolvimento, recentemente disponibilizado para consulta
pública).<p>
                <p>Foram encontrados 37 itens, indicando financiamentos supostamente
dispensados por sete agências bilaterais de cooperação (Alemanha, Áustria,
Canadá, Espanha, Estados Unidos, Noruega e Reino Unido) e uma agência
multilateral (o GEF). Os três casos selecionados para servirem como amostra
foram retirados deste universo.</p>
                <p><a href="bd.php">Lista de projetos - AOD declarada como financiamento climático (1998-2008)</a></p>
                <p>Nossa finalidade, ao discutir estes exemplos, é confrontar as justificativas e
os objetivos de cada um deles com seus resultados, seu alcance e relevância,
em termos das necessidades globais e domésticas tidas como mais prementes
(tanto pelos próprios acordos de cooperação internacional já firmados, no
campo das mudanças climáticas, como por atores locais em situação de
vulnerabilidade). Isto é: avaliar quão <b>eficientes</b> são essas iniciativas, para a
resolução dos problemas identificados; quão <b>transparentes</b>, e <b>equânimes</b>
também, na distribuição dos ônus, benefícios, riscos e responsabilidades,
entre países e atores, no enfrentamento das causas e consequências da
mudança do clima.</p>
