                <h1>Pivôs conceituais</h1>
                <p>Esses fatos evidenciam-se através de dois acontecimentos em particular: 1)
a criação do Painel Intergovernamental sobre Mudanças Climáticas (IPCC,
na sigla em inglês), em 1988; e 2) a Convenção-Quadro das Nações Unidas
sobre Mudanças Climáticas (UNFCCC, na sigla em inglês), tratado redigido e
assinado por 154 países durante a Conferência das Nações Unidas sobre Meio
Ambiente e Desenvolvimento, que realizou-se no Rio de Janeiro em 1992.
A intenção desta pesquisa é verificar como tais desdobramentos traduziram-
se no campo da ajuda externa, ou seja: de que modo a cooperação oficial
Norte-Sul, uma das práticas nucleares das relações internacionais no período
do pós-guerra, foi obrigada a responder a essa mudança de paradigmas, na
qual ideologias e práticas, até então centradas em objetivos de crescimento
econômico e produtividade, viram-se confrontadas por exigências de
preservação ambiental e de precaução em relação ao futuro, tendo em vista
a viabilidade ecológica do planeta. Mais especificamente, a pesquisa examina
como a problemática do aquecimento global foi incorporada aos objetivos da
ajuda oficial ao desenvolvimento (AOD), e em que medida essa modalidade
particular de auxílio - denominada <b>financiamento climático</b> - tende, ou
não, a refletir os problemas distributivos de costume, no que toca à questão
do clima, e a reproduzir a tradicional assimetria de poder, e de interesses,
característica das iniciativas de cooperação direcionadas pelos países centrais
àqueles periféricos.<p>
                <p>Para efeito de delimitação conceitual, o financiamento climático abordado
neste estudo consiste na transferência de recursos financeiros, seja sob
a forma de subvenções plenas ou de empréstimos subsidiados, realizada
pelos governos dos países desenvolvidos no intuito de auxiliar os países
em desenvolvimento a cumprirem duas tarefas consideradas urgentes e
necessárias: 1) adaptarem-se àqueles impactos das mudanças climáticas
tidos como inevitáveis (processo denominado <b>adaptação</b>); b) reduzirem
suas emissões de gás de efeito estufa (processo conhecido como <b>mitigação</b>),
adotando trajetórias de desenvolvimento limpo. Não serão considerados aqui
outros tipos de financiamento climático Norte-Sul, como aqueles oriundos do
capital privado, ou obtidos através do mercado de créditos de carbono, ou
ainda a compensação financeira recebida como resultado da adesão ao REDD
(Redução das Emissões por Desmatamento e Degradação florestal, ou REDD,
na sigla em inglês).</p>
                <p>O elemento-chave na definição de financiamento climático é que seu objetivo
principal, explicitamente declarado, ou então <i>um de seus resultados mais
importantes</i> deve ser a mitigação ou a adaptação (por exemplo, o sequestro
ou redução das emissões de gases de efeito estufa [GEE], ou a diminuição da
vulnerabilidade de populações afetadas pelas mudanças climáticas).</p>
