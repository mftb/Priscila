                <h1>Resumo</h1>
                <p>Na primeira parte do trabalho, apresentamos as perspectivas teóricas e as
categorias analíticas em torno das quais a pesquisa estruturou-se. Na segunda
parte, focalizamos o estado atual da cooperação para o desenvolvimento,
tanto sob o ponto de vista da prática institucional reinante, como do debate
político que se trava a seu respeito. Na terceira parte, traçamos um perfil da
cooperação de cunho ambiental e, mais particularmente, daquela voltada
para as mudanças climáticas. Finalmente, na quarta parte, a título de
ilustração, examinamos três projetos de cooperação custeados no Brasil por
organismos multi ou bilaterais, entre 1998 e 2008, que tinham as mudanças
climáticas como objetivo principal.<p>
                <p>Esses projetos são avaliados a partir de alguns dos princípios normativos
que têm sido propostos, de forma mais recorrente, para guiar as políticas
de financiamento climático, dentre os quais destacam-se: 1) a <b>eficiência</b>,
ou seja, sua adequação e relevância para solucionar os problemas que
abordam; 2) a <b>equidade</b>, isto é, como são concebidos e implementados,
em termos da alocação de custos, benefícios, riscos e responsabilidades
entre países e atores; e 3) a <b>transparência</b>, ou seja, até que ponto são
mensurados, reportados e verificados, com completude e precisão, os fluxos
de financiamento transferidos do Norte para o Sul.</p>
                <p>Os resultados da pesquisa corroboram algumas das principais críticas
imputadas ao campo da cooperação ambiental: o descolamento entre
discurso e prática, a falta de <i>accountability</i> e de transparência pública, a
ênfase em perspectivas econômicas e soluções de mercado, e a manutenção
de certas hierarquias e desigualdades já preexistentes, quando se trata de
distribuir papeis, direitos e deveres entre os atores envolvidos.</p>
