                    <h2><a href="http://www.sciencedirect.com/science/article/pii/S0305750X11002014">The money trail: ranking donor transparency in foreign aid</a></h2>
                    <img src="money_trail.jpg" />
                    <p>Anirban Ghosh e Homi Kharas (Set. 2011)</p>
                    <p>A transparência das atividades de ajuda externa tem sido reconhecida como uma
das principais áreas em que a eficácia da AOD podem ser melhorada. Neste trabalho, os
autores propõem um índice para avaliar e classificar os doadores quanto à transparência
de suas atividades de cooperação internacional. O Índice de Transparência desenvolvido
por eles classifica 31 agências doadoras bilaterais e multilaterais segundo seis medidas de
transparência. Uma das descobertas deste estudo é que ser um membro da IATI (International
Aid Transparency Initiative) é um forte indicador de que um doador irá revelar-se mais
transparente na maioria das outras dimensões também. Nenhuma relação é encontrada
entre a transparência e os volumes de ajuda externa dispensados pelos doadores. De uma
maneira geral, a International Development Association e a Austrália são identificadas como os
doadores mais transparentes, enquanto que a Coréia do Sul e o Inter-American Development
Bank Special Fund são os menos transparentes.</p>
