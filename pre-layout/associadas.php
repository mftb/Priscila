                    <h2><a href="http://www.iesp.uerj.br/corpo-docente/crsmilani/">Cooperação Sul-Sul e agendas de política externa em perspectiva comparada</a></h2>
                    <img src="south_south.jpg" />
                    <p>Carlos R.S. Milani (Abr. 2012)</p>
                    <p>A pesquisa, ainda em andamento, visa a analisar como as estratégias de
Cooperação Sul-Sul (CSS) se integram nas agendas de política externa de seis países hoje
considerados potências emergentes: África do Sul, Brasil, China, Índia, México e Turquia.
Também chamados "<i>new powers</i>", "grandes países periféricos" ou "<i>rising states</i>", os países de
renda média que foram selecionados para a pesquisa começam a tornar qualitativamente mais
densa a sua participação no sistema de cooperação internacional para o desenvolvimento, não
mais apenas enquanto beneficiários, mas também como doadores. Financiam projetos e
prestam cooperação técnica em áreas das mais diversas, incluindo saúde pública, educação
fundamental, meio ambiente, intercâmbio universitário, educação não formal, projetos de
assistência técnica, desenvolvimento agrícola, cooperação tecnológica e científica, gestão
pública, bem como criação de infraestruturas. Atuam em parceria com países em
desenvolvimento de diferentes regiões do mundo (África, América Latina, Oriente Médio e
Ásia). A definição de estratégias de CSS em suas políticas externas é concomitante à
importância que passam a desempenhar na agenda política e econômica internacional,
particularmente nos processos de reforma da governança global e de reconfiguração de
alianças regionais e coalizões inter-regionais.</p>
