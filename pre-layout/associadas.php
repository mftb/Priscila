                    <h2><a href="http://www.sciencedirect.com/science/article/pii/S0305750X11001987">What determines the size of aid projects?</a></h2>
                    <img src="projects_reality_tiny.jpg" />
                    <p>Christopher Kilby (Ago. 2011)</p>
                    <p>Ao longo dos últimos anos, considerável atenção tem sido dedicada à fragmentação
da ajuda externa, à proliferação de doadores e de projetos nos países em desenvolvimento.
A fragmentação da cooperação continua a aumentar, apesar dos esforços internacionais para
promover a coordenação entre doadores. Um possível resultado da fragmentação é que os
projetos tornam-se menores, o que implica em mais trabalho administrativo para os governos
beneficiários, já sobrecarregados, para cada dólar de ajuda recebida. Esta pesquisa utiliza
dados da plataforma AidData sobre compromissos bilaterais de cooperação, setores e agências
de financiamento para explorar os fatores determinantes do tamanho dos projetos e para
melhor compreender as forças motrizes da fragmentação da ajuda externa. Na medida em
que o tamanho do projeto é impulsionado pela composição setorial ou propósito da ajuda, os
custos administrativos associados podem ser justificados. Variações devidas a outros fatores,
como, por exemplo, a estrutura administrativa de um doador, ou interesses burocráticos,
constituem um poderoso argumento em favor de reformas.</p>
