                    <h2><a href="http://revista.ibict.br/liinc/index.php/liinc/article/view/466">Dinâmicas de conhecimento na cooperação internacional para o meio ambiente: uma análise do ônibus movido a hidrogênio no Brasil com recursos do Fundo para o Meio Ambiente Mundial (GEF)</a></h2>
                    <img src="onibus.jpg" />
                    <p>Nicole Aguilar Gayard (Ago. 2011)</p>
                    <p>No contexto de crescentes preocupações com a dimensão ambiental na
agenda política global, tem havido um aumento de iniciativas de cooperação internacional
voltadas para esta temática. O Fundo para o Meio Ambiente Mundial (GEF), criado em 1991,
constitui um organismo multilateral, cujos financiamentos destinam-se a projetos para a
proteção do meio ambiente global, realizados em países em desenvolvimento. A proposta do
GEF se enquadra no âmbito das assimetrias Norte-Sul para gerir os problemas ambientais,
consistindo em um instrumento de auxílio aos países com menos recursos para participar na
prevenção de problemas ambientais globais. A pesquisa examina as dinâmicas de
conhecimento envolvidas neste padrão de cooperação. Com base no referencial teórico do
neofuncionalismo nas relações internacionais, discute-se o papel central que formas de
<i>expertise</i> adquiriram na realização da cooperação internacional em geral, e mais
especificamente, nas iniciativas voltadas ao meio ambiente, tendo sido incorporadas na
estrutura das principais agências multilaterais de cooperação. A importância da <i>expertise</i> no
estabelecimento de padrões e políticas ambientais é contraposta por abordagens - como os
Estudos Sociais da Ciência e Tecnologia e a Teoria Cultural do Risco - que questionam a
aparente neutralidade do conhecimento científico neste processo. Assim, argumenta-se que o
maior nível de capacidades científicas e tecnológicas no Norte permite que este tenha uma
posição privilegiada no momento de definir as políticas e opções de cooperação para o meio
ambiente. Este referencial é utilizado para analisar um projeto de cooperação realizado no
Brasil com recursos do GEF, para o desenvolvimento e teste de um protótipo de ônibus
movido a hidrogênio. Foi possível concluir, com base neste projeto, que alternativas
tecnológicas constituem um dos focos da cooperação internacional para o meio ambiente, e
que a realização dos projetos permanece fortemente centrada na participação de <i>experts</i>. Por
outro lado, promove-se a disseminação do uso de tecnologias e de conhecimentos associados
a modelos de gestão ambiental, mas perpetua-se a dependência de tecnologias dos países em
desenvolvimento em relação aos países desenvolvidos. Além disso, a participação do Sul na
definição de agendas ambientais permanece prejudicada.</p>
