                    <h2><a href="http://www.sciencedirect.com/science/article/pii/S0305750X1100204X">More dollars than sense: refining our knowledge of development finance using AidData</a></h2>
                    <img src="foreign_aid.jpg" />
                    <p>Michael J. Tierney, Daniel L. Nielson, Darren G. Hawkins, J. Timmons Roberts, Michael G. Findley, Ryan M. Powers, Bradley Parks, Sven E. Wilson e Robert L. Hicks (Nov. 2011)</p>
                    <p>Neste ensaio, os autores apresentam um novo banco de dados sobre ajuda
oficial ao desenvolvimento (AOD), intitulado AidData, o qual contempla maior número de
doadores bilaterais e multilaterais do que os outros bancos de dados já existentes, ao mesmo
tempo em que aprofunda informações, no nível dos projetos, sobre os objetivos e atividades
que são financiados pela ajuda externa. Estes dados são utilizados para oferecer uma visão
geral de certas tendências importantes na cooperação internacional. Alguns dos temas
discutidos são: transparência, "novos doadores" (não descritos ou analisados previamente),
alocação e eficácia da ajuda externa. A tese do artigo é que as pesquisas atualmente
conduzidas com o auxílio do AidData vêm refinar, ao invés de revolucionar, a compreensão
deste campo. Os autores consideram que atualmente os debates sobre AOD são respaldados
por pouca informação, e que as afirmações feitas tendem a basear-se em evidências muito
limitadas ou fracas. A ajuda externa não é uma solução simples, mas tampouco é causa
suficiente da maioria dos problemas dos países em desenvolvimento; suas motivações, sua
distribuição e seus efeitos são complexos e cambiantes. Capturar essa complexidade requer
conjuntos de dados bem detalhados, análise rigorosa e métodos sofisticados, que permitam
aos pesquisadores realizar inferências causais e descritivas sobre os fenômenos estudados.</p>
