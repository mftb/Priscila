                    <h2><a href="http://onlinelibrary.wiley.com/doi/10.1111/j.1467-9493.2012.00452.x/full">Contextualizing other political ecologies: Japan's environmental aid to the Philippines</a></h2>
                    <p>Soyeun Kim (Mar. 2012)</p>
                    <p>A ecologia política de um projeto japonês de AOD ambiental certamente não é
um fenômeno simples, tal como apresentado no objetivo declarado de "conservar o meio
ambiente e seus preciosos recursos naturais", mas sim um assunto condicionado por forças
conjunturais multiescalares. Para uma melhor compreensão deste doador não-ocidental
específico, e também de outros doadores, o artigo analisa as fontes contextuais das mudanças
ambientais em relação a duas questões centrais: a primeira diz respeito à forma como a
agenda verde internacional é repolitizada e traduzida, tanto na prática da ajuda externa
japonesa como no contexto local das Filipinas; a segunda diz respeito a como as relações de
poder entre doador e recipiente (e as várias forças mais amplas que os circundam) podem
levar a resultados inesperados, à medida em que o processo de cooperação se desenrola.</p>
