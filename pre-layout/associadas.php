                    <h2><a href="http://www.tandfonline.com/doi/abs/10.3200/ENVT.51.1.8-21">Has foreign aid been greened?</a></h2>
                    <img src="menino_brasileiro.jpg" />
                    <p>J. Timmons Roberts, Bradley C. Parks, Michael J. Tierney & Robert L. Hicks (Ago. 2010)</p>
                    <p>Os países em desenvolvimento afirmam que não podem, e não devem , arcar com os
custos envolvidos na proteção ambiental, uma vez que as nações industrializadas só puderam
alcançar altos padrões de vida graças a um longo processo de industrialização intensivo
em recursos naturais e destruidor do meio ambiente - e que agora teria chegado a vez dos
países do Sul global seguirem essa via, ao industrializarem-se também. Durante a Rio-92, os
países ricos comprometeram-se a aumentar substancialmente seus aportes destinados à
cooperação ambiental (<i>green aid</i>) nos países pobres; estes últimos temiam, entretanto, que
um incremento nessa área acabasse por suplantar a ajuda externa direcionada às necessidades
humanas básicas e ao desenvolvimento econômico, tidos como mais prementes. Considerando
que as promessas de "ajuda verde" são sistematicamente reiteradas, cabe perguntar em que
medida os países doadores têm efetivamente honrado os compromissos que assumiram.
Até que ponto as agências bilaterais, os organismos multilaterais e os bancos regionais de
fomento efetivamente "ecologizaram" sua ajuda externa? Os autores discorrem sobre como,
até recentemente, era difícil, se não impossível, responder a essas questões, devido à falta
de informações estatísticas padronizadas e confiáveis sobre ajuda externa. Essa falta estaria
sendo suprida atualmente pelo banco de dados PLAID.</p>
