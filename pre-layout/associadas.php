                    <h2><a href="http://www.sciencedirect.com/science/article/pii/S0305750X11001951">Coding error or statistical embellishment? The political economy of reporting climate aid</a></h2>
                    <p>Axel Michaelowa e Katharina Michaelowa (Out. 2011)</p>
                    <p>De maneira a beneficiar-se do apoio popular, bastante difundido atualmente, no
que diz respeito às políticas climáticas, as agências de cooperação internacional esforçam-se
para demonstrar a importância desse tema em suas atividades de ajuda ao desenvolvimento.
Os autores do artigo procuram verificar se esse fato pode levar a falsas declarações oficiais,
politicamente motivadas; para isso, usam os dados disponíveis sobre os projetos de AOD em
geral, e sobre a política nacional de 21 países doadores do CAD, de 1995 a 2007. Através da
busca por determinadas palavras-chave nas descrições dos projetos individuais, e codificação
manual complementar, todas as atividades ditas de 'financiamento climático' são avaliadas
quanto ao seu real conteúdo, e a partir daí são construídas as variáveis de controle mais
relevantes. Os resultados econométricos revelam que, de fato, a codificação dos projetos é
sistematicamente influenciada pela orientação ideológica dos governos doadores, assim como
pelas preferências ambientais dos eleitores nacionais.</p>
