                    <h2><a href="http://www.google.com/url?sa=t&rct=j&q=%22aiddata%22%20econometric&source=web&cd=12&ved=0CCAQFjABOAo&url=http%3A%2F%2Fkms1.isn.ethz.ch%2Fserviceengine%2FFiles%2FISN%2F132738%2Fipublicationdocument_singledocument%2Ff586eb41-7081-4b8f-8c85-e02e3e8aa24e%2Fen%2Fwp69.pdf&ei=7qDFTq6HJoXy0gHtweSTDw&usg=AFQjCNGHrJP-8HBJpXTAHEN6ghGWzIJyrw&sig2=vDvHqGjH6CT6TAFOv1n-tg">Development cooperation and climate change: political-economic determinants of adaptation aid</a></h2>
                    <img src="gee.jpg" />
                    <p>Katharina Michaelowa e Axel Michaelowa (2011)</p>
                    <p>Desde a Conferência das Nações Unidas sobre Meio Ambiente e
Desenvolvimento, realizada no Rio de Janeiro, em 1992, as agências bilaterais e multilaterais
enfatizam que a ajuda ao desenvolvimento tem se orientado cada vez mais para intervenções
climaticamente corretas. No que toca à mitigação, estudos anteriores indicam que ela
corresponde às declarações feitas pelos próprios doadores, mas que reflete-se muito pouco na
alocação da ajuda externa. Neste artigo, os autores examinam o caso da adaptação. Através de
extensiva pesquisa, feita por busca de palavras-chave e codificação manual das descrições de
projetos individuais, eles criam um sistema de codificação específico para a
categoria 'adaptação' das atividades de cooperação. Depois de agregá-las a um painel de
doadores-ano, obtêm um conjunto de dados que permite testar empiricamente o efeito do
processo de negociação internacional do clima e de variáveis políticas, específicas dos
doadores individuais, que podem refletir o desenvolvimento de preferências verdes, por parte
dos eleitores, na ajuda externa alocada para a adaptação. Os autores concluem que, ao
contrário da ajuda destinada à mitigação, aquela dirigida à adaptação é significantemente
influenciada por esses fatores. Embora esta descoberta não esteja de acordo com a retórica
dos próprios doadores, ela está bem alinhada com o foco no desenvolvimento da ajuda
bilateral, o qual tende a deixar a provisão de bens públicos globais (tais como a mitigação)
para outras áreas das políticas públicas, e concentra suas atividades relacionadas às mudanças
climáticas naquelas especificamente relevantes para o país destinatário.</p>
