                    <h2><a href="http://labmundo.org/">A agenda ambiental na cooperação internacional para o desenvolvimento: discutindo conflitos entre o Estado e a sociedade civil na cooperação Sul-Sul do Brasil.</a></h2>
                    <img src="lusofonia.jpg" />
                    <p>Tássia Camila de Oliveira Carvalho (Mar. 2012)</p>
                    <p>Desde o final da Segunda Guerra Mundial, a cooperação internacional para o
desenvolvimento (CID) encontra posição de destaque nas agendas internacionais dos países
ricos, na promoção de políticas públicas voltadas ao desenvolvimento dos países mais pobres.
Mais recentemente, temos observado um período de rearticulação da CID em que países
tradicionalmente receptores de AOD passam a promover cooperação como uma importante
ferramenta diplomática na disputa por poder (<i>soft power</i>), através de um modelo de
cooperação mais horizontal: a cooperação Sul-Sul (CSS). Neste contexto, o Brasil desponta
como um destacado ator que transita da condição de receptor para a de doador de CID.
Apesar do discurso de "solidariedade", os ganhos, sobretudo políticos, do Brasil são inegáveis:
a visibilidade frente a instituições financeiras internacionais e a ascensão enquanto liderança
regional estão entre os principais. Entretanto, a rearticulação e a horizontalização da CID
representaram de fato uma mudança de nível estrutural, ou apenas ajudaram a reafirmar
valores e padrões de desenvolvimento pautados nas mesmas perspectivas ideológicas que
regem a cooperação dos países ricos? Partindo da percepção de dubiedade entre ampliação da
autonomia política e reprodução da dependência, a questão ecológica não pode negligenciada
do debate. Os padrões de acumulação de capital e o crescimento econômico são responsáveis
por uma crise ambiental insolúvel a curto e médio prazo. Não teriam os países pobres,
principais alvos da escassez e má distribuição de recursos, um papel fundamental para
ressignificar não apenas as formas de CID, mas da própria noção de desenvolvimento? Se a CSS
tem se tornado alvo de interesse político e constantemente apresenta-se como dotada de uma
<i>expertise</i> diferenciada, de condições menos impositivas, menos imperialistas e mais
consensuadas, é do nosso interesse compreender se a lógica desenvolvimentista tende a ser
superada ou reproduzida. Assim, propomos um mapeamento de políticas publicas realizadas
através da CSS com foco nas questões ecológicas.</p>
