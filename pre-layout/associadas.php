                    <h2><a href="http://www.usc.es/~economet/reviews/ijaeqs911.pdf">Environmental aid and economic development in the Third World</a></h2>
                    <p>B. Mak Arvin, Zafar Kayani & Marisa A. Scigliano (2009)</p>
                    <p>As mudanças climáticas exercem um impacto profundo sobre o planeta, especialmente
nos países em desenvolvimento, como destaca o Relatório Stern, feito para o governo
britânico em 2006. Uma das soluções para mitigar a degradação ambiental e obter melhores
resultados parece ser através do oferecimento de ajuda externa aos países pobres. Utilizando
novos dados, disponíveis através do banco de dados PLAID (Project-Level Aid), o artigo indaga
o que determina o nível de cooperação ambiental dispensada aos países em desenvolvimento
- e em especial, se essa assistência é influenciada pelo grau de desenvolvimento econômico do
país destinatário. Ao mesmo tempo, os autores investigam se o desenvolvimento econômico
é afetado pelo recebimento de AOD ambiental. Implícita na segunda questão, é claro, está
a ideia de que, além de visar resultados ecológicos, a cooperação ambiental também possui
potencial para aumentar a prosperidade econômica dos países pobres.</p>
