                    <h2><a href="http://politics.as.nyu.edu/docs/IO/16188/north.pdf">A theory of North-South climate finance</a></h2>
                    <img src="nsd.jpg" />
                    <p>Johannes Urpelainen (Ago. 2010)</p>
                    <p>As emissões de dióxido de carbono (CO2) estão crescendo rapidamente no mundo em
desenvolvimento, mas apenas os países industrializados possuem recursos suficientes para
financiar atividades adequadas de mitigação e de descarbonização no Sul global. Neste artigo,
o autor examina a economia política do financiamento climático. Com base em pesquisas
anteriores, ele desenvolve um modelo formal, incluindo as três principais questões que
envolvem este tipo de financiamento: a) os incentivos para o doador e a participação do
recipiente; b) a capacitação; e c) a relação entre os financiamentos público e privado. A análise
formal produz vários novos resultados. Primeiro, a capacitação de sucesso parece apontar para
uma redução na necessidade de oferecer transferências diretas de renda para os beneficiários.
Em segundo lugar, o financiamento privado irá dotar os doadores com incentivos para
melhorar a capacitação, aumentando assim o valor do financiamento climático mais do que
se pensava anteriormente. O artigo também discute as implicações empíricas e as implicações
práticas, em termos de políticas públicas, desses achados.</p>
