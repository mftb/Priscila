                    <h2><a href="http://www.sciencedirect.com/science/article/pii/S0305750X11002026">Rhetoric versus reality: the best and worst of aid agency practices</a></h2>
                    <p>William Easterly e Claudia R. Williamson (Nov. 2011)</p>
                    <p>Os críticos da ajuda externa, assim como os seus defensores, os países recipientes e
os doadores têm produzido uma retórica eloquente sobre a necessidade de melhores práticas
de cooperação - mas será que isso vem se tornando realidade? Este artigo tenta monitorar o
melhor e o pior nas práticas de cooperação das agências bilaterais, multilaterais e da ONU. Os
autores criaram medidas de avaliação para as práticas no campo da ajuda externa, baseadas
na transparência, na especialização, na seletividade, em canais de assistência tidos como
ineficazes, e nos custos indiretos. Descobre-se, dentre outras coisas, que o desempenho médio
de todas as agências - em matéria de transparência, de fragmentação e de seletividade - ainda
é muito baixo.</p>
